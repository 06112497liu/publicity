public class BranchInfo {
    
    /** 分支机构名称   */
    private String name;

    
    /** 分支机构信用代码或注册号   */
    private String code;


    public String getName() {
        return name;
    }


    public void setName(String name) {
        this.name = name;
    }


    public String getCode() {
        return code;
    }


    public void setCode(String code) {
        this.code = code;
    }

}

